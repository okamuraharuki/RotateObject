using UnityEngine;

public class DontDestroyCanvas : MonoBehaviour
{
    static DontDestroyCanvas _instance;
    private void Awake()
    {
        if (_instance != null)
        {
            Destroy(gameObject);
        }
        else
        {
            _instance = this;
            DontDestroyOnLoad(gameObject);
        }
    }
}
